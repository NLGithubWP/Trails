# Helper function for formatting the y-axis
def mb_formatter(x, pos):
    return '{:.1f}'.format(x)


# Helper function to calculate memory usage at specific workload percentages
def print_memory_usage_at_percentages(memory_log, percentages):
    for label, data in memory_log.items():
        timestamps = [x[0] for x in data]
        memory_usage = [x[1] / 1024 for x in data]  # Convert KB to MB

        for percentage in percentages:
            index = int(len(timestamps) * (percentage / 100))
            if index >= len(timestamps):
                index = len(timestamps) - 1

            print(f"Memory usage for '{label}' "
                  f"after {percentage}% of workload: {memory_usage[index]:.2f} MB"
                  f"Time usage {timestamps[index]}")


